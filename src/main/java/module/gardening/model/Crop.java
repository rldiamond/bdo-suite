package module.gardening.model;

public class Crop {

    private String seed;
    private String crop;

    public Crop() {

    }

    public String getSeed() {
        return seed;
    }

    public String getCrop() {
        return crop;
    }

    public void setSeed(String seed) {
        this.seed = seed;
    }

    public void setCrop(String crop) {
        this.crop = crop;
    }
}
